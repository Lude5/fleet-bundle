def shade(hex_color, pct=-15):
    """Darken or lighten a hex color."""
    h = hex_color.lstrip('#')
    r, g, b = int(h[0:2], 16), int(h[2:4], 16), int(h[4:6], 16)
    f = (100 + pct) / 100
    r = max(0, min(255, int(r * f)))
    g = max(0, min(255, int(g * f)))
    b = max(0, min(255, int(b * f)))
    return f'#{r:02x}{g:02x}{b:02x}'


def copy_template(slug):
    dest = ROOT / slug
    if dest.exists():
        raise SystemExit(f'Folder already exists: {dest}')
    print(f'Copying template {TEMPLATE} → {dest}')
    shutil.copytree(TEMPLATE, dest, ignore=shutil.ignore_patterns(
        '__pycache__', '*.pyc', 'data', '.env', 'analytics.db', '*.db'))
    (dest / 'data').mkdir(exist_ok=True)
    return dest


def write_favicon(dest, name, color):
    """Generate a simple favicon with the first letter of the brand."""
    letter = name.strip()[0].upper()
    svg = f'''<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 64 64" fill="none">
  <rect width="64" height="64" rx="14" fill="{color}"/>
  <text x="32" y="44" font-family="Arial Black, sans-serif" font-size="34" font-weight="900" fill="#000" text-anchor="middle">{letter}</text>
</svg>
'''
    (dest / 'static' / 'favicon.svg').write_text(svg, encoding='utf-8')
    print(f'  ✓ favicon.svg ({letter} on {color})')


def split_name(full):
    """Split 'Acme Finds' → ('Acme', 'Finds'). Fallback to (full, '')."""
    parts = full.strip().split(maxsplit=1)
    if len(parts) == 2:
        return parts[0], parts[1]
    # No space: split mid-word if long enough
    if len(full) > 5:
        mid = len(full) // 2
        return full[:mid], full[mid:]
    return full, ''


